    public void atualizarTabela() {
        try {
            List<Categoria> lstCategorias = new CategoriaDAO().getAll();

            TableModel tableModelProdutos = TableModelCreator.createTableModel(Categoria.class, lstCategorias, null);
            tbCategorias.setModel(tableModelProdutos);
            mudaEstadoBotoes(false);
        } catch (Exception e) {
            System.out.println("Houve um erro ao tentar popular a tabela");
        }
    }

    private void mudaEstadoBotoes(boolean novoEstado) {
        btnEditar.setEnabled(novoEstado);
        btnExcluir.setEnabled(novoEstado);
    }
