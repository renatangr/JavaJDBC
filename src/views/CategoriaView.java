    private void tbCategoriasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbCategoriasMouseClicked
        JTable source = (JTable)evt.getSource();
        
        int row = source.rowAtPoint( evt.getPoint() );
        int column = tbCategorias.convertColumnIndexToView(tbCategorias.getColumn("Id ").getModelIndex());
                 
        String s = source.getModel().getValueAt(row, column).toString();
        //JOptionPane.showMessageDialog(null, s);

        idSelecionado = Integer.parseInt(s);
        
        mudaEstadoBotoes(true);

    }//GEN-LAST:event_tbCategoriasMouseClicked

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        new CategoriaDAO().excluir(idSelecionado);
        
        atualizarTabela();
        
        mudaEstadoBotoes(false);
    }//GEN-LAST:event_btnExcluirActionPerformed

